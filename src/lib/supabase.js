import { createClient } from '@supabase/supabase-js';

const supabaseUrl = import.meta.env.VITE_SUPABASE_URL;
const supabaseAnonKey = import.meta.env.VITE_SUPABASE_ANON_KEY;

// Check if keys are available. If not, we might want to warn or just not initialize properly.
// For the MVP, we'll initialize it, but usage should guard against missing keys.

export const supabase = (supabaseUrl && supabaseAnonKey) 
  ? createClient(supabaseUrl, supabaseAnonKey)
  : null;

if (!supabase) {
  console.warn('Supabase URL or Anon Key missing. Authentication and Database features will not work.');
}
